    private void K_btn_logoutActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_K_btn_logoutActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin logout?",
                "Konfirmasi Logout",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            UserSession.clearSession(); // Hapus sesi
            this.dispose(); // Tutup dashboard
            new halaman_login().setVisible(true); // Buka halaman login
        }
    }// GEN-LAST:event_K_btn_logoutActionPerformed

