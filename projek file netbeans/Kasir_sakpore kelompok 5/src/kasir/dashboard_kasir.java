    private void setupDashboardKeyBindings() {
    // Dapatkan InputMap dan ActionMap dari root pane JFrame
    InputMap inputMap = this.getRootPane().getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW);
    ActionMap actionMap = this.getRootPane().getActionMap();

    // -- Shortcut ALT + 1 untuk Beranda --
    KeyStroke alt1KeyStroke = KeyStroke.getKeyStroke(KeyEvent.VK_1, KeyEvent.ALT_DOWN_MASK);
    inputMap.put(alt1KeyStroke, "berandaAction");
    actionMap.put("berandaAction", new AbstractAction() {
        @Override
        public void actionPerformed(ActionEvent e) {
            K_btn_beranda.doClick(); // Simulasikan klik tombol Beranda
        }
    });

    // -- Shortcut ALT + 2 untuk Kasir --
    KeyStroke alt2KeyStroke = KeyStroke.getKeyStroke(KeyEvent.VK_2, KeyEvent.ALT_DOWN_MASK);
    inputMap.put(alt2KeyStroke, "kasirAction");
    actionMap.put("kasirAction", new AbstractAction() {
        @Override
        public void actionPerformed(ActionEvent e) {
            K_btn_kasir.doClick(); // Simulasikan klik tombol Kasir
        }
    });

    // -- Shortcut ALT + 3 untuk Daftar Barang --
    KeyStroke alt3KeyStroke = KeyStroke.getKeyStroke(KeyEvent.VK_3, KeyEvent.ALT_DOWN_MASK);
    inputMap.put(alt3KeyStroke, "daftarBarangAction");
    actionMap.put("daftarBarangAction", new AbstractAction() {
        @Override
        public void actionPerformed(ActionEvent e) {
            K_btn_daftar_barang.doClick(); // Simulasikan klik tombol Daftar Barang
        }
    });

    // -- Shortcut ALT + SHIFT + ENTER untuk Logout --
    KeyStroke altShiftEnterKeyStroke = KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, KeyEvent.ALT_DOWN_MASK | KeyEvent.SHIFT_DOWN_MASK);
    inputMap.put(altShiftEnterKeyStroke, "logoutAction");
    actionMap.put("logoutAction", new AbstractAction() {
        @Override
        public void actionPerformed(ActionEvent e) {
            K_btn_logout.doClick(); // Simulasikan klik tombol Logout
        }
    });
}

