    private void loadTableData(String searchTerm) {
        tableModel.setRowCount(0);
        String sql = "SELECT id_barang, nama_barang, kategori, harga_pokok, ppn, harga_jual, stok FROM daftar_barang";

        if (searchTerm != null && !searchTerm.isEmpty()) {
            sql += " WHERE nama_barang ILIKE ? OR id_barang ILIKE ? OR kategori ILIKE ?";
        }
        sql += " ORDER BY id_barang ASC";

        try (Connection conn = Koneksi.getConnection();
                PreparedStatement ps = conn.prepareStatement(sql)) {

            if (searchTerm != null && !searchTerm.isEmpty()) {
                String searchPattern = "%" + searchTerm + "%";
                ps.setString(1, searchPattern);
                ps.setString(2, searchPattern);
                ps.setString(3, searchPattern);
            }

            ResultSet rs = ps.executeQuery();
            int no = 1;
            while (rs.next()) {
                tableModel.addRow(new Object[] {
                        no++,
                        rs.getString("id_barang"),
                        rs.getString("nama_barang"),
                        rs.getString("kategori"),
                        rs.getLong("harga_pokok"),
                        rs.getLong("ppn"),
                        rs.getLong("harga_jual"),
                        rs.getInt("stok")
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

