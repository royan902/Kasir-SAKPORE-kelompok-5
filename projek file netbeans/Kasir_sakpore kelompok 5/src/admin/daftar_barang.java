    private void A_btn_editActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_A_btn_editActionPerformed
        // TODO add your handling code here:
        if (selectedItemId == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih barang yang ingin di-edit dari tabel.");
            return;
        }
        javax.swing.JFrame mainFrame = (javax.swing.JFrame) this.getTopLevelAncestor();
        if (mainFrame instanceof dashboard_admin) {
            ((dashboard_admin) mainFrame).showEditBarangPanel(selectedItemId);
        }
    }// GEN-LAST:event_A_btn_editActionPerformed

    private void A_btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_A_btn_deleteActionPerformed
        // TODO add your handling code here:
        if (selectedItemId == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih barang yang ingin dihapus dari tabel.");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus barang ini?",
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM daftar_barang WHERE id_barang = ?";
                try (Connection conn = Koneksi.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {
                    ps.setString(1, selectedItemId);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Barang berhasil dihapus!");
                    loadTableData(null);
                    selectedItemId = null;
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }// GEN-LAST:event_A_btn_deleteActionPerformed

