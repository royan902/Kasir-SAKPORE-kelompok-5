    private void loadUserData(String searchTerm) {
        tableModel.setRowCount(0);

        // Query untuk mengambil semua data user dari tabel "user"
        // Ingat untuk menggunakan tanda kutip ganda karena "user" adalah keyword SQL
        String sql = "SELECT username, password, jenis_kelamin, jabatan, telepon, alamat FROM \"user\"";

        if (searchTerm != null && !searchTerm.isEmpty()) {
            sql += " WHERE username ILIKE ? OR jabatan ILIKE ? OR alamat ILIKE ?";
        }
        sql += " ORDER BY username ASC";

        try (Connection conn = Koneksi.getConnection();
                PreparedStatement ps = conn.prepareStatement(sql)) {

            if (searchTerm != null && !searchTerm.isEmpty()) {
                String pattern = "%" + searchTerm + "%";
                ps.setString(1, pattern);
                ps.setString(2, pattern);
                ps.setString(3, pattern);
            }

            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                tableModel.addRow(new Object[] {
                        rs.getString("username"),
                        rs.getString("password"),
                        rs.getString("jenis_kelamin"),
                        rs.getString("jabatan"),
                        rs.getString("telepon"),
                        rs.getString("alamat")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data pengguna: " + e.getMessage());
            e.printStackTrace();
        }
    }

