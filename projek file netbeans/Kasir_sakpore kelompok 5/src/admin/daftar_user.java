    private void A_btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_A_btn_deleteActionPerformed
        // TODO add your handling code here:
        if (selectedUsername == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih pengguna yang ingin dihapus dari tabel.", "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus pengguna '" + selectedUsername + "'?", "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM \"user\" WHERE username = ?";
                try (Connection conn = Koneksi.getConnection(); PreparedStatement ps = conn.prepareStatement(sql)) {
                    ps.setString(1, selectedUsername);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Pengguna berhasil dihapus!");
                    clearForm();
                    loadUserData(null);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus pengguna: " + e.getMessage());
            }
        }
    }// GEN-LAST:event_A_btn_deleteActionPerformed

    private void A_btn_editActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_A_btn_editActionPerformed
        // TODO add your handling code here:
        if (selectedUsername == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih pengguna yang ingin di-edit dari tabel.", "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int selectedRow = A_tabel_daftar_user.getSelectedRow();

        // === PERBAIKAN LOGIKA NULL-SAFE UNTUK MENGAMBIL DATA DARI TABEL ===

        // Ambil nilai dari tabel dan simpan di variabel sementara
        Object usernameObj = tableModel.getValueAt(selectedRow, 0);
        Object passwordObj = tableModel.getValueAt(selectedRow, 1);
        Object jenisKelaminObj = tableModel.getValueAt(selectedRow, 2);
        Object jabatanObj = tableModel.getValueAt(selectedRow, 3);
        Object teleponObj = tableModel.getValueAt(selectedRow, 4);
        Object alamatObj = tableModel.getValueAt(selectedRow, 5);

        // Set nilai ke komponen, cek null terlebih dahulu
        A_tf_username.setText(usernameObj != null ? usernameObj.toString() : "");
        A_tf_password.setText(passwordObj != null ? passwordObj.toString() : "");
        A_cb_kelamin
                .setSelectedItem(jenisKelaminObj != null ? jenisKelaminObj.toString() : "-- Pilih Jenis Kelamin --");
        A_cb_jabatan.setSelectedItem(jabatanObj != null ? jabatanObj.toString() : "-- Pilih Jabatan --");
        A_tf_telepon.setText(teleponObj != null ? teleponObj.toString() : "");
        A_tf_alamat.setText(alamatObj != null ? alamatObj.toString() : "");

        A_btn_simpan.setText("UPDATE");
        A_tf_username.setEditable(false);
    }// GEN-LAST:event_A_btn_editActionPerformed

