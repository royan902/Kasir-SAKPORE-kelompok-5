    private void A_btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_A_btn_simpanActionPerformed
        // TODO add your handling code here:
        String username = A_tf_username.getText().trim();
        String password = A_tf_password.getText().trim();
        String jenisKelamin = A_cb_kelamin.getSelectedItem().toString();
        String jabatan = A_cb_jabatan.getSelectedItem().toString();
        String alamat = A_tf_alamat.getText().trim();
        String telepon = A_tf_telepon.getText().trim();

        if (username.isEmpty() || password.isEmpty() || A_cb_kelamin.getSelectedIndex() == 0
                || A_cb_jabatan.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Username, Password, Jenis Kelamin, dan Jabatan harus diisi!",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = Koneksi.getConnection()) {
            PreparedStatement ps;
            if ("UPDATE".equals(A_btn_simpan.getText())) {
                // Logika UPDATE tidak berubah, sudah benar
                String sql = "UPDATE \"user\" SET password=?, jenis_kelamin=?, jabatan=?, telepon=?, alamat=? WHERE username=?";
                ps = conn.prepareStatement(sql);
                ps.setString(1, password);
                ps.setString(2, jenisKelamin);
                ps.setString(3, jabatan);
                ps.setString(4, telepon);
                ps.setString(5, alamat);
                ps.setString(6, selectedUsername);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data pengguna berhasil diperbarui!");
            } else { // Mode SIMPAN BARU
                // === PERBAIKAN LOGIKA UNTUK MENAMBAH USER BARU ===

                // 1. Ambil ID tertinggi yang sudah ada
                int newId = 1; // Default jika tabel masih kosong
                try (Statement stmt = conn.createStatement();
                        ResultSet rs = stmt.executeQuery("SELECT MAX(id) FROM \"user\"")) {
                    if (rs.next()) {
                        newId = rs.getInt(1) + 1;
                    }
                }

                // 2. Query INSERT sekarang menyertakan kolom 'id'
                String sql = "INSERT INTO \"user\" (id, username, password, jenis_kelamin, jabatan, telepon, alamat) VALUES (?, ?, ?, ?, ?, ?, ?)";
                ps = conn.prepareStatement(sql);
                ps.setInt(1, newId); // Set ID yang baru dibuat
                ps.setString(2, username);
                ps.setString(3, password);
                ps.setString(4, jenisKelamin);
                ps.setString(5, jabatan);
                ps.setString(6, telepon);
                ps.setString(7, alamat);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Pengguna baru berhasil ditambahkan!");
            }
            ps.close();

            clearForm();
            loadUserData(null);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace();
        }
    }// GEN-LAST:event_A_btn_simpanActionPerformed

