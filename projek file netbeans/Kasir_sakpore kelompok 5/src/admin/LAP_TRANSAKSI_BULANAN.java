    private void loadTransactionData(int year, int month, String searchTerm) {
        tableModel.setRowCount(0);
        
        String sql = "SELECT id_transaksi, tanggal, kasir, total_akhir, bayar, kembalian, metode_pembayaran FROM transaksi";
        
        try (Connection conn = Koneksi.getConnection()) {
            PreparedStatement ps;
            
            if (searchTerm != null && !searchTerm.isEmpty()) {
                // Mode Pencarian: Cari berdasarkan ID atau Kasir
                sql += " WHERE id_transaksi ILIKE ? OR kasir ILIKE ?";
                ps = conn.prepareStatement(sql);
                String pattern = "%" + searchTerm + "%";
                ps.setString(1, pattern);
                ps.setString(2, pattern);
            } else {
                // Mode Filter: Filter berdasarkan Tahun dan Bulan
                sql += " WHERE EXTRACT(YEAR FROM CAST(tanggal AS TIMESTAMP)) = ? AND EXTRACT(MONTH FROM CAST(tanggal AS TIMESTAMP)) = ? ORDER BY tanggal DESC";
                ps = conn.prepareStatement(sql);
                ps.setInt(1, year);
                ps.setInt(2, month);
            }

            ResultSet rs = ps.executeQuery();
            int no = 1;
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy HH:mm");
            while (rs.next()) {
                tableModel.addRow(new Object[]{
                    no++,
                    rs.getString("id_transaksi"),
                    dateFormat.format(rs.getTimestamp("tanggal")),
                    rs.getString("kasir"),
                    rs.getLong("total_akhir"),
                    rs.getLong("bayar"),
                    rs.getLong("kembalian"),
                    rs.getString("metode_pembayaran")
                });
            }
            rs.close();
            ps.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data transaksi: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
    private void applyFilter() {
        int year = filter_tahun.getYear();
        int month = filter_bulan.getMonth() + 1; // JMonthChooser 0-indexed
        loadTransactionData(year, month, null);
    }

