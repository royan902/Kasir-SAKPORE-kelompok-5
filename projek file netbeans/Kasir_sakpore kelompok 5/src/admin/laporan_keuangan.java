    private void loadFinancialData(int year, int month) {
        tableModel.setRowCount(0);

        // Query SQL yang sudah diperbaiki dengan CAST untuk kolom pemasukan dan
        // pengeluaran
        String sql = "(SELECT " +
                "    CAST(tanggal AS TIMESTAMP)::date AS tanggal, " +
                "    'Transaksi Harian ' || TO_CHAR(CAST(tanggal AS TIMESTAMP)::date, 'DD-MM-YYYY') AS jenis_keuangan, "
                +
                "    SUM(CAST(total_akhir AS BIGINT)) AS pemasukan, " + // <-- PERBAIKAN DI SINI
                "    0 AS pengeluaran " +
                "FROM transaksi " +
                "WHERE EXTRACT(YEAR FROM CAST(tanggal AS TIMESTAMP)) = ? AND EXTRACT(MONTH FROM CAST(tanggal AS TIMESTAMP)) = ? "
                +
                "GROUP BY CAST(tanggal AS TIMESTAMP)::date) " +
                "UNION ALL " +
                "(SELECT " +
                "    CAST(tanggal AS TIMESTAMP)::date AS tanggal, " +
                "    'Pembelian Stok ' || nama_barang AS jenis_keuangan, " +
                "    0 AS pemasukan, " +
                "    CAST(total_harga AS BIGINT) AS pengeluaran " + // <-- PERBAIKAN DI SINI
                "FROM pembelian_barang " +
                "WHERE EXTRACT(YEAR FROM CAST(tanggal AS TIMESTAMP)) = ? AND EXTRACT(MONTH FROM CAST(tanggal AS TIMESTAMP)) = ?) "
                +
                "ORDER BY tanggal ASC";

        try (Connection conn = Koneksi.getConnection();
                PreparedStatement ps = conn.prepareStatement(sql)) {

            // Set parameter untuk bagian transaksi
            ps.setInt(1, year);
            ps.setInt(2, month);
            // Set parameter untuk bagian pembelian_barang
            ps.setInt(3, year);
            ps.setInt(4, month);

            ResultSet rs = ps.executeQuery();
            int no = 1;
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd MMMM yyyy", locale);

            while (rs.next()) {
                tableModel.addRow(new Object[] {
                        no++,
                        dateFormat.format(rs.getDate("tanggal")),
                        rs.getString("jenis_keuangan"),
                        rs.getLong("pemasukan"),
                        rs.getLong("pengeluaran")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat laporan keuangan: " + e.getMessage());
            e.printStackTrace();
        }

        // Setelah data dimuat, hitung totalnya
        calculateTotals();
    }

    private void calculateTotals() {
        long totalPemasukan = 0;
        long totalPengeluaran = 0;

        for (int i = 0; i < tableModel.getRowCount(); i++) {
            totalPemasukan += (long) tableModel.getValueAt(i, 3); // Kolom Pemasukan
            totalPengeluaran += (long) tableModel.getValueAt(i, 4); // Kolom Pengeluaran
        }

        // Format angka menjadi format mata uang (Rp)
        label_total_pemasukan_bulanan.setText(currencyFormat.format(totalPemasukan));
        label_total_pengeluaran_bulanan.setText(currencyFormat.format(totalPengeluaran));
    }

    private void applyFilter() {
        int year = filter_YearChooser1.getYear();
        int month = filter_MonthChooser1.getMonth() + 1; // JMonthChooser 0-indexed
        loadFinancialData(year, month);
    }

