    private void loadPurchaseData(String searchTerm) {
        tableModel.setRowCount(0); // Kosongkan tabel sebelum diisi data baru
        
        // Query dasar untuk mengambil semua data
        String sql = "SELECT * FROM pembelian_barang";
        
        // Jika ada kata kunci pencarian, tambahkan filter WHERE
        if (searchTerm != null && !searchTerm.isEmpty()) {
            sql += " WHERE CAST(tanggal AS TEXT) ILIKE ? OR id_barang ILIKE ? OR kategori ILIKE ? OR nama_supplier ILIKE ? OR perusahaan_supplier ILIKE ?";
        }
        sql += " ORDER BY tanggal DESC"; // Urutkan berdasarkan tanggal terbaru

        try (Connection conn = Koneksi.getConnection(); 
             PreparedStatement ps = conn.prepareStatement(sql)) {

            if (searchTerm != null && !searchTerm.isEmpty()) {
                String pattern = "%" + searchTerm + "%";
                ps.setString(1, pattern);
                ps.setString(2, pattern);
                ps.setString(3, pattern);
                ps.setString(4, pattern);
                ps.setString(5, pattern);
            }

            ResultSet rs = ps.executeQuery();
            int no = 1;
            // Format tanggal agar lebih mudah dibaca (dd-MM-yyyy)
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");

            while (rs.next()) {
                tableModel.addRow(new Object[]{
                    no++,
                    dateFormat.format(rs.getDate("tanggal")), // Format tanggalnya
                    rs.getString("id_barang"),
                    rs.getString("nama_barang"),
                    rs.getString("kategori"),
                    rs.getString("satuan"),
                    rs.getLong("harga_satuan"),
                    rs.getInt("jumlah_beli"),
                    rs.getLong("total_harga"),
                    rs.getString("nama_supplier"),
                    rs.getString("perusahaan_supplier")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data laporan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

