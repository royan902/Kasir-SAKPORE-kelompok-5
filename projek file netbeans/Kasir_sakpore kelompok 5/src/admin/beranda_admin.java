    private void loadDashboardData() {
        try (Connection conn = Koneksi.getConnection()) {
            // 1. Ambil Jumlah Barang Tersedia
            try (PreparedStatement ps = conn.prepareStatement("SELECT COUNT(*) FROM daftar_barang");
                    ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    A_lbl_jumlah_daftar_barang.setText(rs.getInt(1) + " BARANG");
                }
            }

            // 2. Ambil Jumlah Stok Kurang dari 5
            try (PreparedStatement ps = conn
                    .prepareStatement("SELECT COUNT(*) FROM daftar_barang WHERE CAST(stok AS INTEGER) < 5");
                    ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    A_lbl_jumlah_stok_habis.setText(rs.getInt(1) + " BARANG");
                }
            }

            // 3. Ambil Jumlah Transaksi Hari Ini
            try (PreparedStatement ps = conn
                    .prepareStatement("SELECT COUNT(*) FROM transaksi WHERE tanggal::date = CURRENT_DATE");
                    ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    A_lbl_jumlah_transaksi_harian.setText(rs.getInt(1) + " TRANSAKSI");
                }
            }

            // 4. Ambil Jumlah Pendapatan Hari Ini
            try (PreparedStatement ps = conn.prepareStatement(
                    "SELECT SUM(CAST(total_akhir AS BIGINT)) FROM transaksi WHERE tanggal::date = CURRENT_DATE");
                    ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    A_lbl_jumlah_pendapatan_harian.setText(currencyFormat.format(rs.getLong(1)));
                }
            }

            // 5. Ambil Pemasukan & Pengeluaran Bulanan (Bulan Ini)
            int currentMonth = java.time.LocalDate.now().getMonthValue();
            int currentYear = java.time.LocalDate.now().getYear();

            // Pemasukan
            String sqlPemasukan = "SELECT SUM(CAST(total_akhir AS BIGINT)) FROM transaksi WHERE EXTRACT(YEAR FROM CAST(tanggal AS TIMESTAMP)) = ? AND EXTRACT(MONTH FROM CAST(tanggal AS TIMESTAMP)) = ?";
            try (PreparedStatement ps = conn.prepareStatement(sqlPemasukan)) {
                ps.setInt(1, currentYear);
                ps.setInt(2, currentMonth);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    A_lbl_jumlah_pemasukkan_bulanan.setText(currencyFormat.format(rs.getLong(1)));
                }
            }

            // Pengeluaran
            String sqlPengeluaran = "SELECT SUM(CAST(total_harga AS BIGINT)) FROM pembelian_barang WHERE EXTRACT(YEAR FROM CAST(tanggal AS TIMESTAMP)) = ? AND EXTRACT(MONTH FROM CAST(tanggal AS TIMESTAMP)) = ?";
            try (PreparedStatement ps = conn.prepareStatement(sqlPengeluaran)) {
                ps.setInt(1, currentYear);
                ps.setInt(2, currentMonth);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    A_lbl_jumlah_penggeluaran.setText(currencyFormat.format(rs.getLong(1)));
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data dasbor: " + e.getMessage());
            e.printStackTrace();
        }
    }

